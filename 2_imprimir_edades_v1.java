public class Imprimir_Vector_Edades {
    public static void main(String args[]) {
       /*           VECTORES          */
       // Para crear un array o vector se realiza: 
       //   Tipo_de_variable[ ]   Nombre_del_array = new  Tipo_de_variable[dimensión];
       
       
       
       int[ ] listaEdades = {45, 23, 11, 9}; // se dimensiona un vector de tamaño N y se le asignan valores

      System.out.println(listaEdades[0]); // imprimire el valor asignado en la posición 0
      System.out.println(listaEdades[1]); // imprimire el valor asignado en la posición 1
      System.out.println(listaEdades[2]); // imprimire el valor asignado en la posición 2
      System.out.println(listaEdades[3]); // imprimire el valor asignado en la posición 3
      System.out.println(listaEdades[4]); // imprimire el valor asignado en la posición 4
    }
}